package com.example.forexeventalarm.model;

public class Event {
    private String date;
    private String time;
    private String title;
    private String impact;

    public String getDate() {
        return date;
    }

    public String getTime() {
        return time;
    }

    public String getTitle() {
        return title;
    }

    public String getImpact() {
        return impact;
    }
}
